package practise.math;

public class test {
    public static void main(String[] args) {

        System.out.println(Math.abs(-10));//取参数-10的绝对值

        System.out.println(Math.ceil(2.1));//向上取整

        System.out.println(Math.floor(2.1));//向下取整

        System.out.println(Math.pow(2,3));//取2的三次方

        System.out.println(Math.round(2.4)+" "+Math.round(2.5));//四舍五入取整

    }
}
